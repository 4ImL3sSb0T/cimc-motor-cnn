"""
全局配置 — 所有参数集中管理

=== 为什么集中管理? ===
把所有参数放在一个地方，好处:
1. 修改参数只需改一处，不用到处找
2. 保证所有模块用的是同一套参数
3. 方便对照固件参数，确保 PC 端和 ESP32 端一致

=== 参数分组 ===
1. FFT 参数: 频谱变换相关的参数 (与 ESP32 固件对齐)
2. CNN 输入: 神经网络输入数据的形状
3. 数据处理: 数据预处理相关
4. CNN 训练: 训练相关的超参数和路径
"""

from pathlib import Path

# ══════════════════════════════════════════════════════════════════════
# FFT 参数 — 与 ESP32 固件 imu_data_process.h 保持一致
# ══════════════════════════════════════════════════════════════════════

# FFT 点数: 一次 FFT 变换处理多少个采样点
# 必须是 2 的幂 (512, 1024, 2048...)
# 越大 → 频率分辨率越高，但计算量也越大
SP_FFT_SIZE = 1024

# 跳步大小: 相邻两帧之间的偏移量
# 比 FFT_SIZE 小 → 帧之间有重叠 → 时间分辨率更高
# 256 = 1024/4，即 75% 重叠
SP_HOP_SIZE = 256

# 频率 bin 数: FFT 输出的有效频率点数
# 实数信号的 FFT 结果是对称的，只需要前一半
# 1024 / 2 = 512 个频率 bin
SP_FREQ_BINS = SP_FFT_SIZE // 2          # 512

# 采样率: IMU 传感器每秒采集多少个点
# 6667 Hz = 每秒 6667 个采样点
# 这决定了能分析的最高频率 = 采样率/2 = 3333.5 Hz (奈奎斯特定理)
SP_SAMPLE_RATE = 6667.0                   # Hz

# 频率分辨率: 每个频率 bin 代表多少 Hz
# = 采样率 / FFT点数 = 6667 / 1024 ≈ 6.51 Hz
# 意思是: bin 0 = 0 Hz, bin 1 = 6.51 Hz, bin 2 = 13.02 Hz, ...
SP_FREQ_RES = SP_SAMPLE_RATE / SP_FFT_SIZE  # 6.51 Hz

# ══════════════════════════════════════════════════════════════════════
# CNN 输入形状
# ══════════════════════════════════════════════════════════════════════

# 每个 CNN 样本包含多少个时间帧
# 16 帧 × 每帧 0.038 秒 = 约 0.61 秒的信号
# 太少 → 信息不足; 太多 → 计算量大
CNN_SAMPLE_FRAMES = 16

# CNN 输入张量的形状: (通道数, 帧数, 频率bin数)
# 3 通道 = X/Y/Z 三轴加速度
# 16 帧 = 时间维度
# 512 = 频率维度
# 注意: 这是 channels_first 格式，实际送入模型时要转成 channels_last
CNN_INPUT_SHAPE = (3, CNN_SAMPLE_FRAMES, SP_FREQ_BINS)  # (3, 16, 512)

# ══════════════════════════════════════════════════════════════════════
# 数据处理参数
# ══════════════════════════════════════════════════════════════════════

# 去直流偏移时，用前多少行数据来计算直流分量
# 前 10000 行是设备静置时采集的数据 (没有振动)
# 用这些数据算出 X/Y/Z 各自的平均值 (直流偏移)，然后减去
DC_OFFSET_SAMPLES = 10000

# ══════════════════════════════════════════════════════════════════════
# CNN 训练参数
# ══════════════════════════════════════════════════════════════════════

# ── 路径配置 ──────────────────────────────────────────────────────────
# 项目根目录: src/ 的上一级
PROJECT_ROOT = Path(__file__).parent.parent
# 原始数据目录: 存放 xlsx 文件
DATA_DIR = PROJECT_ROOT / "data"
# 输出目录: 存放生成的 .npz 样本
OUTPUT_DIR = PROJECT_ROOT / "output"
# 模型目录: 存放训练好的模型
MODEL_DIR = PROJECT_ROOT / "models"

# ── 分类类别 ──────────────────────────────────────────────────────────
# 定义模型要识别的类别
# !!! 你需要根据实际数据修改这个列表 !!!
# 例如: 如果你只有 "正常" 和 "异常" 两类，改成 ["normal", "abnormal"]
CLASS_NAMES = ["idle", "vibration", "impact", "other"]
# 类别数量 (自动计算)
NUM_CLASSES = len(CLASS_NAMES)

# ── 训练超参数 ────────────────────────────────────────────────────────

# BATCH_SIZE (批大小): 每次训练取多少个样本
# 16 = 每次取 16 个样本算一次梯度
# 太大 → 显存不够; 太小 → 训练不稳定
BATCH_SIZE = 16

# EPOCHS (训练轮数): 把全部训练数据看几遍
# 100 = 最多训练 100 轮 (可能被 EarlyStopping 提前终止)
EPOCHS = 100

# LEARNING_RATE (学习率): 每次更新参数的步幅大小
# 0.001 是 Adam 优化器的常用默认值
# 太大 → 训练不稳定; 太小 → 训练太慢
LEARNING_RATE = 1e-3

# VALIDATION_SPLIT (验证集比例): 多少数据用来验证
# 0.2 = 20% 做验证，80% 做训练
VALIDATION_SPLIT = 0.2

# PATIENCE (早停耐心值): val_loss 连续多少轮不改善就停止训练
# 15 = 如果连续 15 个 epoch val_loss 都没下降，就停止
# 防止训练太久导致过拟合
PATIENCE = 15
